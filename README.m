Project name: Tiles Gallery

 Project Purpose:Tiles Gallery is a modern web application for browsing and exploring premium tiles. Users can view tile collections, inspect tile details, create accounts, and securely authenticate using email/password or Google Sign-In.

## Live URL

https://your-live-site-url.vercel.app

## Key Features

* User Authentication with Better Auth
* Google Sign-In Integration
* Browse Premium Tile Collections
* View Detailed Tile Information
* Protected Routes for Authenticated Users
* Responsive Design for Mobile, Tablet, and Desktop
* Dynamic Routing with Next.js App Router
* Custom 404 Not Found Page

## Technologies Used

* Next.js 16
* React 19
* Tailwind CSS
* DaisyUI
* Better Auth
* MongoDB Atlas
* Next.js App Router

